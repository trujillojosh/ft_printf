#include "../../includes/libft.h"

int		ft_int_len_ext(int nb, int base)
{
	int	i;

	i = 0;
	if (nb < 0)
	{
		nb = nb * -1;
		i++;
	}
	while ((nb / base) != 0)
	{
		i++;
		nb = nb / base;
	}
	i++;
	return (i);
}
